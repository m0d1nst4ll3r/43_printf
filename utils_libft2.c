#include "ft_printf.h"

void	ft_strupper(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] > 'a' && s[i] < 'z')
			s[i] += 'A' - 'a';
		i++;
	}
}
